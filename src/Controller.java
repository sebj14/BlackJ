    public void MoveCrossCard(JLabel LabelC,int Delay)
    {
                Timer time1=new Timer();
                TimerTask task=new TimerTask()
                {
                    int counter=0;
                    public void run()
                    {
                        if(counter==30)
                        {
                            time1.cancel();
                            
                        }
                        LabelC.setLocation(LabelC.getLocation().x-5, LabelC.getLocation().y-5);
                        counter++;
                    }
                };
                time1.scheduleAtFixedRate(task, Delay, 30);
        
    }
