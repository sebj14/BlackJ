    public void MoveVerticalCard(JLabel LabelC,int Delay,int limit)
    {
                Timer time1=new Timer();
                TimerTask task=new TimerTask()
                {
                    int counter=0;
                    public void run()
                    {
                        if(counter==limit)
                        {
                            time1.cancel();
                            
                        }
                        LabelC.setLocation(LabelC.getLocation().x-5, LabelC.getLocation().y);
                        counter++;
                    }
                };
                time1.scheduleAtFixedRate(task, Delay, 30);
        
    }
